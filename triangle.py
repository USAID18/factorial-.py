import turtle

sc=turtle.Screen()
sc.bgcolor("blue")
sc.setup(600,350)
sc.title("first turtle")


pointer=turtle.Turtle()

pointer.forward(100)
pointer.left(120)
pointer.forward(100)

pointer.left(120)
pointer.forward(100)




pointer.penup()
pointer.right(150)
pointer.forward(50)
pointer.pendown()
pointer.right(90)
pointer.forward(100)
pointer.right(120)
pointer.forward(100)
pointer.right(120)
pointer.forward(100     )
turtle.done()